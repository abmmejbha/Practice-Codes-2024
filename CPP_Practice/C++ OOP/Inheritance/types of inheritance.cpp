//there are five types of inheritance in cpp
/*
1.single  inheritance 
        1 super class and 2 sub class 

2.multilevel inheritance
        nested inheritance is called multilevel inheritance

3.hierarchical inheritance
        many child but one parent 

4.multiple inheritance
        one parent > many child > again one parent
        a class can inherit more than one class

        in this type of inheritance a single child class can have multiple 
        parents classes
        
5.hybrid inheritance
        hybrid inheritance is a combination of more
         than one type of inheritance 
*/